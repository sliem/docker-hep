      real*8 function TB_QBV(s,t1,ms1,mg,mt,ms,ms2,mx1,mx2,s2t,susy)

      implicit none 

      real*8 Pi,Nc,Cf,Nf,Ns,alphas,fac,susy 
      real*8 s,t1,u1,ms1,mg,mt,ms,ms2,s2t
      real*8 ms12,mx1,mx2
      real*8 m2(2,8),softin(4)
      real*8 SK3B0A(7),SK3B0B(8),SK3B0C(3),SK3B0D(3,2),SK3BP(4) 
      real*8 SK3C0A(10),SK3C0B(6),SK3C0C(8,2),SK3D0(11,2)
      real*8 SOF1(8) 
      real*8 mur,dec,TB_QBB,STQBV

      Pi=4.D0*atan(1.D0)
      Nc = 3.D0
      Cf = 4.D0/3.D0 
      Nf = 6.D0
      Ns = 6.D0
      alphas = 1.D0

      u1 = - s - t1 
      fac = 1.D0/(64*Pi*Nc**2)

      m2(1,1) = s 
      m2(1,2) = t1 + ms1**2  
      m2(1,3) = u1 + ms1**2 
      m2(1,4) = t1
      m2(1,5) = u1
      m2(2,1) = ms1**2 
      m2(2,2) = ms2**2 
      m2(2,3) = ms**2 
      m2(2,4) = mg**2 
      m2(2,5) = mt**2 
      m2(2,6) = ms1**2 
      m2(2,7) = mx1**2 
      m2(2,8) = mx2**2 
      
      call SCALAR_ARRAY_TB_B_QS(m2,SK3B0A,SK3B0B,SK3B0C,SK3B0D,SK3BP)
      call SCALAR_ARRAY_TB_C_QS(m2,SK3C0A,SK3C0B,SK3C0C,SK3D0)

      softin(1) = s
      softin(2) = t1 
      softin(3) = u1 
      softin(4) = ms1**2 

      call SOFT_ARRAY_LQ(softin,SOF1)

      mur = ms1
      
      dec = 2.D0*alphas/Pi * TB_QBB(s,t1,ms1)*
     &      (  (Ns-2.D0)/12.D0 * susy * log(mur**2/ms**2)
     &       + 1.D0/24.D0             * log(mur**2/ms1**2)
     &       + 1.D0/24.D0      * susy * log(mur**2/ms2**2)
     &       + 1.D0/24.D0      * susy * log(mur**2/mx1**2)
     &       + 1.D0/24.D0      * susy * log(mur**2/mx2**2)
     &       + 1.D0/6.D0              * log(mur**2/mt**2) 
     &       + Nc/6.D0         * susy * log(mur**2/mg**2)   )

      ms12 = ms1**2 

      STQBV =
     &  + Nc*Cf*Pi*alphas**3 * (  - 32./9.*s**(-4)*t1**2 - 32./9.*
     &    s**(-3)*t1 - 32./9.*ms1**2*s**(-3) )
      STQBV = STQBV + Nc*Cf*(Nf-1)*Pi*alphas**3 * (  - 64./9.*s**(-4)*
     &    t1**2 - 64./9.*s**(-3)*t1 - 64./9.*ms1**2*s**(-3) )
      STQBV = STQBV + Nc*Cf**2*Pi*alphas**3 * ( 64*s**(-4)*t1**2 + 64*
     &    s**(-3)*t1 + 64*ms1**2*s**(-3) )
      STQBV = STQBV + Nc**2*Cf*Pi*alphas**3 * (  - 320./9.*s**(-4)*
     &    t1**2 - 320./9.*s**(-3)*t1 - 320./9.*ms1**2*s**(-3) )
      STQBV = STQBV + susy*Nc*Cf*Pi*alphas**3 * ( 32./3.*s**(-4)*t1**2
     &     + 32./3.*s**(-3)*t1 + 32./3.*ms1**2*s**(-3) )
      STQBV = STQBV + susy*Nc*Cf*(Ns-2)*Pi*alphas**3 * ( 64./9.*s**(-4)
     &    *t1**2 + 64./9.*s**(-3)*t1 + 64./9.*ms1**2*s**(-3) )
      STQBV = STQBV + susy*Nc*Cf**2*Pi*alphas**3 * (  - 32*s**(-4)*
     &    t1**2 - 32*s**(-3)*t1 - 32*ms1**2*s**(-3) )
      STQBV = STQBV + susy*Nc**2*Cf*Pi*alphas**3 * ( 224./9.*s**(-4)*
     &    t1**2 + 224./9.*s**(-3)*t1 + 224./9.*ms1**2*s**(-3) )
      STQBV = STQBV + SK3B0A(1)*susy*Nc**2*Cf*Pi*alphas**3 * (  - 128./
     &    3.*mg**2*s**(-5)*t1**2 - 128./3.*mg**2*s**(-4)*t1 - 128./3.*
     &    mg**2*ms1**2*s**(-4) )
      STQBV = STQBV + SK3B0A(2)*Nc*Cf*Pi*alphas**3 * (  - 128./3.*mt**2
     &    *s**(-5)*t1**2 - 128./3.*mt**2*s**(-4)*t1 - 128./3.*mt**2*
     &    ms1**2*s**(-4) )
      STQBV = STQBV + SK3B0A(3)*susy*Nc*Cf**2*Pi*alphas**3 * ( 32*
     &    s**(-4)*t1**2 + 32*s**(-3)*t1 + 32*ms1**2*s**(-3) - 64*ms**2*
     &    s**(-5)*t1**2 - 64*ms**2*s**(-4)*t1 - 64*ms**2*ms1**2*s**(-4)
     &     + 64*mg**2*s**(-5)*t1**2 + 64*mg**2*s**(-4)*t1 + 64*mg**2*
     &    ms1**2*s**(-4) )
      STQBV = STQBV + SK3B0A(4)*susy*Nc*Cf*(Ns-2)*Pi*alphas**3 * ( 128./
     &    3.*ms**2*s**(-5)*t1**2 + 128./3.*ms**2*s**(-4)*t1 + 128./3.*
     &    ms**2*ms1**2*s**(-4) )
      STQBV = STQBV + SK3B0A(5)*susy*Nc*Cf*Pi*alphas**3 * ( 64./3.*
     &    ms2**2*s**(-5)*t1**2 + 64./3.*ms2**2*s**(-4)*t1 + 64./3.*
     &    ms2**2*ms1**2*s**(-4) )
      STQBV = STQBV + SK3B0A(6)*susy*Nc*Cf*Pi*alphas**3 * ( 64./3.*
     &    mx1**2*s**(-5)*t1**2 + 64./3.*mx1**2*s**(-4)*t1 + 64./3.*
     &    ms1**2*mx1**2*s**(-4) )
      STQBV = STQBV + SK3B0A(7)*susy*Nc*Cf*Pi*alphas**3 * ( 64./3.*
     &    mx2**2*s**(-5)*t1**2 + 64./3.*mx2**2*s**(-4)*t1 + 64./3.*
     &    mx2**2*ms1**2*s**(-4) )
      STQBV = STQBV + SK3B0B(1)*Nc*Cf*(Nf-1)*Pi*alphas**3 * ( 64./3.*
     &    s**(-4)*t1**2 + 64./3.*s**(-3)*t1 + 64./3.*ms1**2*s**(-3) )
      STQBV = STQBV + SK3B0B(1)*Nc*Cf**2*Pi*alphas**3 * ( 96*s**(-4)*
     &    t1**2 + 96*s**(-3)*t1 + 96*ms1**2*s**(-3) )
      STQBV = STQBV + SK3B0B(1)*Nc**2*Cf*Pi*alphas**3 * ( 32*
     &    (s-4*ms12)**(-1)*s**(-3)*t1**2 + 32*(s-4*ms12)**(-1)*s**(-2)*
     &    t1 + 8*(s-4*ms12)**(-1)*s**(-1) - 256./3.*s**(-4)*t1**2 - 256.
     &    /3.*s**(-3)*t1 - 8*s**(-2) - 256./3.*ms1**2*s**(-3) )
      STQBV = STQBV + SK3B0B(2)*Nc*Cf*Pi*alphas**3 * ( 16./3.*s**(-4)*
     &    t1**2 + 16./3.*s**(-3)*t1 - 64./3.*ms1**2*s**(-5)*t1**2 - 64./
     &    3.*ms1**2*s**(-4)*t1 + 16./3.*ms1**2*s**(-3) - 64./3.*ms1**4*
     &    s**(-4) )
      STQBV = STQBV + SK3B0B(2)*Nc*Cf**2*Pi*alphas**3 * ( 64*
     &    (s-4*ms12)**(-1)*s**(-3)*t1**2 + 64*(s-4*ms12)**(-1)*s**(-2)*
     &    t1 + 16*(s-4*ms12)**(-1)*s**(-1) + 64*s**(-4)*t1**2 + 64*
     &    s**(-3)*t1 - 16*s**(-2) + 64*ms1**2*s**(-3) )
      STQBV = STQBV + SK3B0B(2)*Nc**2*Cf*Pi*alphas**3 * (  - 32*
     &    (s-4*ms12)**(-1)*s**(-3)*t1**2 - 32*(s-4*ms12)**(-1)*s**(-2)*
     &    t1 - 8*(s-4*ms12)**(-1)*s**(-1) - 32*s**(-4)*t1**2 - 32*
     &    s**(-3)*t1 + 8*s**(-2) - 32*ms1**2*s**(-3) )
      STQBV = STQBV + SK3B0B(3)*susy*Nc**2*Cf*Pi*alphas**3 * (  - 16*
     &    (s-4*ms12)**(-1)*s**(-3)*t1**2 - 16*(s-4*ms12)**(-1)*s**(-2)*
     &    t1 - 4*(s-4*ms12)**(-1)*s**(-1) - 64*(s-4*ms12)**(-1)*mt**2*
     &    s**(-4)*t1**2 - 64*(s-4*ms12)**(-1)*mt**2*s**(-3)*t1 - 16*
     &    (s-4*ms12)**(-1)*mt**2*s**(-2) - 64*(s-4*ms12)**(-1)*mg**2*
     &    s**(-4)*t1**2 - 64*(s-4*ms12)**(-1)*mg**2*s**(-3)*t1 - 16*
     &    (s-4*ms12)**(-1)*mg**2*s**(-2) - 32./3.*s**(-4)*t1**2 - 32./3.
     &    *s**(-3)*t1 + 4*s**(-2) - 32./3.*ms1**2*s**(-3) + 16*mt**2*
     &    s**(-3) + 32*ms**2*s**(-5)*t1**2 + 32*ms**2*s**(-4)*t1 + 32*
     &    ms**2*ms1**2*s**(-4) + 32./3.*mg**2*s**(-5)*t1**2 + 32./3.*
     &    mg**2*s**(-4)*t1 + 16*mg**2*s**(-3) + 32./3.*mg**2*ms1**2*
     &    s**(-4) )
      STQBV = STQBV + SK3B0B(3)*susy*Nc**2*Cf*s2t*Pi*alphas**3 * ( 128*
     &    (s-4*ms12)**(-1)*mg*mt*s**(-4)*t1**2 + 128*(s-4*ms12)**(-1)*
