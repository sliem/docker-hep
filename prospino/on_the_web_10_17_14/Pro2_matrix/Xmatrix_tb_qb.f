     &    s**(-5)*t1**2 + 64*ms**2*s**(-4)*t1 + 64*ms**2*ms1**2*s**(-4)
     &     - 64*mg**2*s**(-5)*t1**2 - 64*mg**2*s**(-4)*t1 - 64*mg**2*
