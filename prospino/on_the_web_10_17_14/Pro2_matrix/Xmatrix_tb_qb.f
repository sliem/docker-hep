     &    (s-4*ms12)**(-1)*mt**2*s**(-2) + 64*(s-4*ms12)**(-1)*mg**2*
     &    s**(-4)*t1**2 + 64*(s-4*ms12)**(-1)*mg**2*s**(-3)*t1 + 16*
     &    (s-4*ms12)**(-1)*mg**2*s**(-2) + 16*s**(-4)*t1**2 + 16*
     &    s**(-3)*t1 - 4*s**(-2) + 16*ms1**2*s**(-3) - 16*mt**2*s**(-3)
     &     - 16*mg**2*s**(-3) )
      STQBV = STQBV + SK3B0B(4)*susy*Nc**2*Cf*s2t*Pi*alphas**3 * (  - 
     &    128*(s-4*ms12)**(-1)*mg*mt*s**(-4)*t1**2 - 128*
     &    (s-4*ms12)**(-1)*mg*mt*s**(-3)*t1 - 32*(s-4*ms12)**(-1)*mg*mt
     &    *s**(-2) + 32*mg*mt*s**(-3) )
      STQBV = STQBV + SK3B0B(5)*susy*Nc*Cf*(Ns-2)*Pi*alphas**3 * ( 32./
     &    3.*s**(-4)*t1**2 + 32./3.*s**(-3)*t1 + 32./3.*ms1**2*s**(-3)
     &     - 128./3.*ms**2*s**(-5)*t1**2 - 128./3.*ms**2*s**(-4)*t1 - 
     &    128./3.*ms**2*ms1**2*s**(-4) )
