     &    s**(-3)*t1 + 8*s**(-2) - 32*ms1**2*s**(-3) + 32*mt**2*s**(-3)
     &     + 32*mg**2*s**(-3) )
      STQBV = STQBV + SK3B0B(4)*susy*Nc*Cf**2*s2t*Pi*alphas**3 * ( 256*
     &    (s-4*ms12)**(-1)*mg*mt*s**(-4)*t1**2 + 256*(s-4*ms12)**(-1)*
     &    mg*mt*s**(-3)*t1 + 64*(s-4*ms12)**(-1)*mg*mt*s**(-2) - 64*mg*
