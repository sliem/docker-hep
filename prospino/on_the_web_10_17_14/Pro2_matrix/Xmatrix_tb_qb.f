      real*8 function TB_QB1(s,t1,ms1,sca)

      implicit none 

      real*8 Pi,Nc,Cf,Nf,alphas
      real*8 s,t1,u1,ms1,sca 
      real*8 conalp,consof,TB_QBB

      Pi=4.D0*atan(1.D0)
      Nc = 3.D0
      Cf = 4.D0/3.D0 
      Nf = 6.D0
      alphas = 1.D0
      
      u1 = - s - t1 

      conalp = alphas/2.D0/Pi * TB_QBB(s,t1,ms1)
     &    * ( 11.D0/3.D0*Nc - 2.D0/3.D0 * (Nf-1.D0)) * 2.0*log(sca/ms1)

      consof = -alphas*Cf/2.D0/Pi * TB_QBB(s,t1,ms1)
     &       * ( - 2.D0 * log(-t1/ms1**2)
     &           - 2.D0 * log(-u1/ms1**2)
     &           + 3.D0                  ) * 2.0*log(sca/ms1)
 
      TB_QB1 = conalp + consof 

      end
c --------------------------------------------------------------------
      real*8 function TB_QB2(s,t1,s4,ms1,del,s4max,sca)

      implicit none 

      real*8 Pi,Cf,alphas
      real*8 s,t1,s4,del,s4max,ms1,sca 
      real*8 dldel1,TB_QBB

      Pi=4.D0*atan(1.D0)
      Cf = 4.D0/3.D0 
      alphas = 1.D0
      
      dldel1 = log(s4max/ms1**2)
     &       - ( s4max - del )/s4 

      TB_QB2 = -alphas*Cf/2.D0/Pi * TB_QBB(s,t1,ms1)
     &      * 4.D0*dldel1 * 2.0*log(sca/ms1)

      end

c --------------------------------------------------------------------
      real*8 function TB_QB3(s,t1,s4,ms1,sca)

      implicit none 

      real*8 Pi,Nc,Cf,alphas,fac 
      real*8 s,t1,u1,s4,ms1,sca 
      real*8 COLO1(9)

      Pi=4.D0*atan(1.D0)
      Nc = 3.D0
      Cf = 4.D0/3.D0 
      alphas = 1.D0
      
      u1 = s4 - s - t1 

      fac = s4 /(s4+ms1**2) /8.D0 /(16*Pi**2)**2 /Nc**2
      
      COLO1(9) = -2.0*log(sca/ms1)

      TB_QB3 = 0.D0
      TB_QB3=TB_QB3 + COLO1(9)*Nc*Cf**2*Pi**4*alphas**3 * ( 1024*ms1**2
     +    *s**(-4)*t1**(-1)*u1 + 1024*ms1**2*s**(-4)*t1*u1**(-1) + 4096
     +    *ms1**2*s**(-4)*t1*s4**(-1) - 4096*ms1**2*s**(-4)*t1**2*
     +    s4**(-2) - 2048*ms1**2*s**(-4) - 1024*ms1**2*s**(-3)*t1**(-2)
     +    *u1 + 1024*ms1**2*s**(-3)*t1**(-1) - 1024*ms1**2*s**(-3)*t1*
     +    u1**(-2) - 4096*ms1**2*s**(-3)*t1*s4**(-2) + 1024*ms1**2*
     +    s**(-3)*u1**(-1) - 2048*ms1**2*s**(-3)*s4**(-1) - 1024*ms1**2
     +    *s**(-2)*t1**(-2) - 1024*ms1**2*s**(-2)*u1**(-2) - 1024*
     +    ms1**4*s**(-3)*t1**(-2) + 2048*ms1**4*s**(-3)*t1**(-1)*
     +    s4**(-1) - 1024*ms1**4*s**(-3)*u1**(-2) + 2048*ms1**4*s**(-3)
     +    *u1**(-1)*s4**(-1) - 4096*ms1**4*s**(-3)*s4**(-2) + 1024*
     +    s**(-4)*t1**(-1)*u1**2 + 3072*s**(-4)*t1 + 1024*s**(-4)*t1**2
     +    *u1**(-1) - 4096*s**(-4)*t1**2*s4**(-1) - 1024*s**(-4)*u1 + 
     +    1024*s**(-3)*t1**(-1)*u1 + 1024*s**(-3)*t1*u1**(-1) - 4096*
     +    s**(-3)*t1*s4**(-1) )

      TB_QB3 = fac * TB_QB3
ctp      TB_QB3 = fac * 389379660.D0 * TB_QB3

      end

c --------------------------------------------------------------------
      real*8 function TB_QBD(s,t1,s4,ms1,del,s4max)

      implicit none 

      real*8 Pi,Nc,Cf,alphas,fac 
      real*8 s,t1,u1,ms1,s4,del,s4max 
      real*8 xw,COLO1(9),dldel1,dldel2 
      real*8 m2(5)

      Pi=4.D0*atan(1.D0)
      Nc = 3.D0
      Cf = 4.D0/3.D0 
      alphas = 1.D0
      
      u1 = - s - t1 
      xw = sqrt(1.D0-4.D0*ms1**2/s)

      m2(1) = s 
      m2(2) = t1 
      m2(3) = u1 
      m2(4) = s4
      m2(5) = ms1**2 

      call LOGAS_LQ(m2,COLO1)

      dldel1 = log(s4max/ms1**2)
     &       - ( s4max - del )/s4 
      dldel2 = log(s4max/ms1**2)**2 
     &       - 2.D0* ( s4max - del )/s4 * log(s4/ms1**2)

      fac = 1.D0/(64*Pi*Nc**2)
      
      TB_QBD =
     +  + Nc*Cf**2*Pi*alphas**3*dldel1 * ( 128*ms1**2*s**(-3) + 128*
     +    s**(-4)*t1**2 + 128*s**(-3)*t1 )
      TB_QBD=TB_QBD + Nc*Cf**2*Pi*alphas**3*dldel2 * (  - 128*ms1**2*
     +    s**(-3) - 128*s**(-4)*t1**2 - 128*s**(-3)*t1 )
      TB_QBD=TB_QBD + COLO1(1)*Nc*Cf**2*Pi*alphas**3*dldel1 * (  - 256*
     +    ms1**2*s**(-5)*t1**2*xw**(-1) - 256*ms1**2*s**(-4)*t1*
     +    xw**(-1) + 128*ms1**2*s**(-3)*xw**(-1) - 256*ms1**4*s**(-4)*
     +    xw**(-1) + 128*s**(-4)*t1**2*xw**(-1) + 128*s**(-3)*t1*
     +    xw**(-1) )
      TB_QBD=TB_QBD + COLO1(1)*Nc**2*Cf*Pi*alphas**3*dldel1 * ( 128*
     +    ms1**2*s**(-5)*t1**2*xw**(-1) + 128*ms1**2*s**(-4)*t1*
     +    xw**(-1) - 64*ms1**2*s**(-3)*xw**(-1) + 128*ms1**4*s**(-4)*
     +    xw**(-1) - 64*s**(-4)*t1**2*xw**(-1) - 64*s**(-3)*t1*xw**(-1)
     +     )
      TB_QBD=TB_QBD + COLO1(3)*Nc*Cf**2*Pi*alphas**3*dldel1 * (  - 128*
     +    ms1**2*s**(-3) - 128*s**(-4)*t1**2 - 128*s**(-3)*t1 )
      TB_QBD=TB_QBD + COLO1(3)*Nc**2*Cf*Pi*alphas**3*dldel1 * ( 64*
     +    ms1**2*s**(-3) + 64*s**(-4)*t1**2 + 64*s**(-3)*t1 )
      TB_QBD=TB_QBD + COLO1(4)*Nc*Cf**2*Pi*alphas**3*dldel1 * (  - 384*
     +    ms1**2*s**(-3) - 384*s**(-4)*t1**2 - 384*s**(-3)*t1 )
      TB_QBD=TB_QBD + COLO1(4)*Nc**2*Cf*Pi*alphas**3*dldel1 * ( 128*
     +    ms1**2*s**(-3) + 128*s**(-4)*t1**2 + 128*s**(-3)*t1 )
      TB_QBD=TB_QBD + COLO1(8)*Nc*Cf**2*Pi*alphas**3*dldel1 * ( 640*
     +    ms1**2*s**(-3) + 640*s**(-4)*t1**2 + 640*s**(-3)*t1 )
      TB_QBD=TB_QBD + COLO1(8)*Nc**2*Cf*Pi*alphas**3*dldel1 * (  - 256*
     +    ms1**2*s**(-3) - 256*s**(-4)*t1**2 - 256*s**(-3)*t1 )

      TB_QBD = fac * TB_QBD 
ctp      TB_QBD = fac * 389379660.D0 * TB_QBD 

      end 

