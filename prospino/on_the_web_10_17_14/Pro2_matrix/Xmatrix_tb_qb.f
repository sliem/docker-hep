     &    t1**2 + 64./3.*s**(-3)*t1 + 64./3.*ms1**2*s**(-3) + 128./3.*
     &    mt**2*s**(-5)*t1**2 + 128./3.*mt**2*s**(-4)*t1 + 128./3.*
     &    mt**2*ms1**2*s**(-4) )
      STQBV = STQBV + SK3B0B(4)*susy*Nc*Cf**2*Pi*alphas**3 * (  - 32*
     &    (s-4*ms12)**(-1)*s**(-3)*t1**2 - 32*(s-4*ms12)**(-1)*s**(-2)*
     &    t1 - 8*(s-4*ms12)**(-1)*s**(-1) - 128*(s-4*ms12)**(-1)*mt**2*
     &    s**(-4)*t1**2 - 128*(s-4*ms12)**(-1)*mt**2*s**(-3)*t1 - 32*
     &    (s-4*ms12)**(-1)*mt**2*s**(-2) - 128*(s-4*ms12)**(-1)*mg**2*
     &    s**(-4)*t1**2 - 128*(s-4*ms12)**(-1)*mg**2*s**(-3)*t1 - 32*
