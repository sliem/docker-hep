      real*8 function TB_QBB(s,t1,ms1)

      implicit none 

      real*8 Pi,Nc,Cf,alphas,fac 
      real*8 s,t1,u1,ms1 

      Pi=4.D0*atan(1.D0)
      Nc = 3.D0
      Cf = 4.D0/3.D0 
      alphas = 1.D0
      
      u1 = - s - t1 

      fac = 1.D0/(64*Pi*Nc**2)
      
      TB_QBB = 
     +    Nc*Cf*Pi**2*alphas**2 * (  - 64*ms1**2*s**(-3) + 64*s**(-4)*
     +    t1*u1 )

      TB_QBB = fac * TB_QBB 
ctp      TB_QBB = fac * 389379660.D0 * TB_QBB 

      end

