      end




c --------------------------------------------------------------------
      real*8 function LQ_QBH(s,t1,s4,ms1)

      implicit none 

      real*8 Pi,Nc,Cf,alphas,fac 
      real*8 s,t1,u1,ms1,s4
      real*8 hardin(5)
