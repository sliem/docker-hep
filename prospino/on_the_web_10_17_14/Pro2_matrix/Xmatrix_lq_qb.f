      LQ_QBV = LQ_QBV + SK3C0B(2)*Nc*Cf**2*Pi*alphas**3 * ( -256*ms1**2
     +    *s**(-3)*t1 - 128*ms1**2*s**(-2) + 128*s**(-2)*t1 + 64*
     +    s**(-1) )
      LQ_QBV = LQ_QBV + SK3C0B(2)*Nc**2*Cf*Pi*alphas**3 * ( 16*
     +    (s-4*ms12)**(-1)*s**(-2)*t1**2 + 16*(s-4*ms12)**(-1)*s**(-1)*
     +    t1 + 4*(s-4*ms12)**(-1) + 96*ms1**2*s**(-3)*t1 + 32*ms1**2*
     +    s**(-2) - 48*s**(-3)*t1**2 - 96*s**(-2)*t1 - 36*s**(-1) )
