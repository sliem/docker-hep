     +    ms1**2*s**(-4) + 128./3.*mt**2*s**(-5)*t1**2 + 128./3.*mt**2*
     +    s**(-4)*t1 + 64./3.*ms1**2*s**(-3) + 64./3.*s**(-4)*t1**2 + 
     +    64./3.*s**(-3)*t1 )
      LQ_QBV = LQ_QBV + SK3B0C(1)*Nc*Cf**2*Pi*alphas**3 * (  - 64*
     +    (s-4*ms12)**(-1)*s**(-3)*t1**2 - 64*(s-4*ms12)**(-1)*s**(-2)*
     +    t1 - 16*(s-4*ms12)**(-1)*s**(-1) - 64*ms1**2*s**(-3) - 64*
