     +    384*m1**2*u2*s4**(-1)*t2s**(-2) - 128*m1**2*s4**(-2) + 128*
     +    m1**2*s4**(-1)*u2s**(-1) + 256*m1**2*s4**(-1)*t2s**(-1) - 384
