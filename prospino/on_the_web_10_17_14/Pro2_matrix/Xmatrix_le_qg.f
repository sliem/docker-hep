c                                                                      c
c     THE SCALING FUNCTIONS                                            c
c                                                                      c
c     LE_QG(MASSIN,LUMI)                                                c
c                                                                      c
c     INPUT :                                                          c
c                                                                      c
c       MASSIN(1)  = s                                                 c
c       MASSIN(2)  = t2                                                c
c       MASSIN(3)  = s4                                                c
c       MASSIN(6)  = m1                                                c
c       MASSIN(12) = qr                                                c
c       MASSIN(13) = qf                                                c
c       MASSIN(25) = gamma_s                                           c
c                                                                      c
cccccccccccccccccccccccccccccccccccccccccccccccccccccccccccccccccccccccc
      real*8 function LE_QG(massin,lumi)

      implicit none 

      real*8     massin(1:30),lumi(1:3),pi,Nc,alphas
     &          ,Ar2,st,log_qf,log_all,MM_qg
     &          ,s,t2,s4,m1,t2s,u2,u2s,m12
     &          ,hardfac
     &          ,ANG_fin(0:9,0:9,-2:2,-2:2)

      pi = 4.D0*atan(1.D0)
      Nc = 3.D0

      s    = massin(1)
      t2   = massin(2)
      s4   = massin(3)
      m1   = massin(6)
      m12  = m1**2 

c               the factorization scale 
      log_qf = log( massin(13)**2/m1**2 ) 
      log_all = log_qf - log(s4/m1**2) + log(1.D0+m1**2/s4)

c               real kinematics built in
      t2s = t2 - m1**2 

      u2  = s4 - s - t2 + m1**2 
      u2s = u2 - m1**2 
ctp      u2s = u1
ctp      u2  = u1 + m1**2

c               gs**2=4*pi*alpha_s is cut, re-appears as nlo later 
      Ar2     = 1.D0
      st      = 1.D0
      alphas  = 1.D0/4.D0/pi
      hardfac = 1.D0 

c               the angular functions 
      call ANGULAR_ARRAY_LE_QG(massin,ANG_fin)

c               the form output
c               replacements: [] -> ()
      MM_qg =
     +  + Ar2*Nc**(-1)*st**2*Pi**3*alphas**2*hardfac*log_all * ( 128*
     +    s**(-1)*(s+u2)**(-1)*m1**2*t2*u2*s4**(-2) + 128*s**(-1)*
     +    (s+u2)**(-1)*t2*u2*s4**(-1) + 128*s**(-1)*m1**2*t2*u2**2*
     +    s4**(-2)*t2s**(-2) - 64*s**(-1)*m1**2*t2*s4**(-2) - 64*
     +    s**(-1)*m1**2*u2**2*s4**(-2)*t2s**(-1) - 192*s**(-1)*m1**2*
     +    u2**2*s4**(-1)*t2s**(-2) - 128*s**(-1)*m1**2*s4**(-1) - 192*
     +    s**(-1)*m1**4*u2**2*s4**(-2)*t2s**(-2) + 128*s**(-1)*m1**4*
     +    u2**2*s4**(-1)*t2s**(-3) - 128*s**(-1)*m1**4*s4**(-2) + 128*
     +    s**(-1)*m1**4*s4**(-1)*t2s**(-1) - 128*s**(-1)*m1**6*t2**(-1)
     +    *u2*t2s**(-3) - 128*s**(-1)*m1**6*t2**(-1)*u2s**(-1)*
     +    t2s**(-1) + 128*s**(-1)*m1**6*u2*s4**(-1)*t2s**(-3) + 128*
     +    s**(-1)*m1**6*u2**2*s4**(-2)*t2s**(-3) + 128*s**(-1)*m1**6*
     +    s4**(-2)*t2s**(-1) + 128*s**(-1)*m1**6*s4**(-1)*u2s**(-1)*
     +    t2s**(-1) - 128*s**(-1)*m1**8*t2**(-1)*u2*s4**(-1)*t2s**(-3)
     +     - 128*s**(-1)*m1**8*t2**(-1)*s4**(-1)*u2s**(-1)*t2s**(-1) - 
     +    128*s**(-1)*m1**8*t2**(-1)*t2s**(-3) )
      MM_qg = MM_qg + Ar2*Nc**(-1)*st**2*Pi**3*alphas**2*hardfac*
     + log_all * ( 128*s**(-1)*m1**8*u2*s4**(-2)*t2s**(-3) + 128*
     +    s**(-1)*m1**8*s4**(-2)*u2s**(-1)*t2s**(-1) + 128*s**(-1)*
     +    m1**8*s4**(-1)*t2s**(-3) - 128*s**(-1)*m1**10*t2**(-1)*
     +    s4**(-1)*t2s**(-3) - 128*s**(-1)*m1**10*t2**(-1)*u2s**(-1)*
     +    t2s**(-3) + 128*s**(-1)*m1**10*s4**(-2)*t2s**(-3) + 128*
     +    s**(-1)*m1**10*s4**(-1)*u2s**(-1)*t2s**(-3) - 128*s**(-1)*
     +    m1**12*t2**(-1)*s4**(-1)*u2s**(-1)*t2s**(-3) + 128*s**(-1)*
     +    m1**12*s4**(-2)*u2s**(-1)*t2s**(-3) + 128*s**(-1)*t2*u2**2*
     +    s4**(-1)*t2s**(-2) - 64*s**(-1)*t2*s4**(-1) - 64*s**(-1)*
     +    u2**2*s4**(-1)*t2s**(-1) + 128*s*(t2*u2+s*m12)**(-2)*m1**4 - 
     +    384*s*(t2*u2+s*m12)**(-2)*m1**6*u2*t2s**(-2) + 128*s*
     +    (t2*u2+s*m12)**(-2)*m1**6*s4**(-1) + 256*s*
     +    (t2*u2+s*m12)**(-2)*m1**6*t2s**(-1) - 384*s*
     +    (t2*u2+s*m12)**(-2)*m1**8*u2*s4**(-1)*t2s**(-2) + 256*s*
     +    (t2*u2+s*m12)**(-2)*m1**8*s4**(-1)*t2s**(-1) )
      MM_qg = MM_qg + Ar2*Nc**(-1)*st**2*Pi**3*alphas**2*hardfac*
     + log_all * ( 256*s*(t2*u2+s*m12)**(-2)*m1**8*u2s**(-1)*t2s**(-1)
     +     - 384*s*(t2*u2+s*m12)**(-2)*m1**8*t2s**(-2) + 256*s*
     +    (t2*u2+s*m12)**(-2)*m1**10*s4**(-1)*u2s**(-1)*t2s**(-1) - 384
     +    *s*(t2*u2+s*m12)**(-2)*m1**10*s4**(-1)*t2s**(-2) - 384*s*
     +    (t2*u2+s*m12)**(-2)*m1**10*u2s**(-1)*t2s**(-2) - 384*s*
     +    (t2*u2+s*m12)**(-2)*m1**12*s4**(-1)*u2s**(-1)*t2s**(-2) - 256
     +    *s*(t2*u2+s*m12)**(-1)*m1**4*u2s**(-1)*t2s**(-1) + 512*s*
     +    (t2*u2+s*m12)**(-1)*m1**4*t2s**(-2) - 256*s*
     +    (t2*u2+s*m12)**(-1)*m1**6*s4**(-1)*u2s**(-1)*t2s**(-1) + 512*
     +    s*(t2*u2+s*m12)**(-1)*m1**6*s4**(-1)*t2s**(-2) + 768*s*
     +    (t2*u2+s*m12)**(-1)*m1**6*u2s**(-1)*t2s**(-2) - 384*s*
     +    (t2*u2+s*m12)**(-1)*m1**6*t2s**(-3) + 768*s*
     +    (t2*u2+s*m12)**(-1)*m1**8*s4**(-1)*u2s**(-1)*t2s**(-2) - 384*
     +    s*(t2*u2+s*m12)**(-1)*m1**8*s4**(-1)*t2s**(-3) + 256*s*
     +    (t2*u2+s*m12)**(-1)*m1**8*u2s**(-2)*t2s**(-2) )
      MM_qg = MM_qg + Ar2*Nc**(-1)*st**2*Pi**3*alphas**2*hardfac*
     + log_all * (  - 768*s*(t2*u2+s*m12)**(-1)*m1**8*u2s**(-1)*
     +    t2s**(-3) + 256*s*(t2*u2+s*m12)**(-1)*m1**10*s4**(-1)*
     +    u2s**(-2)*t2s**(-2) - 768*s*(t2*u2+s*m12)**(-1)*m1**10*
     +    s4**(-1)*u2s**(-1)*t2s**(-3) - 384*s*(t2*u2+s*m12)**(-1)*
     +    m1**10*u2s**(-2)*t2s**(-3) - 384*s*(t2*u2+s*m12)**(-1)*m1**12
     +    *s4**(-1)*u2s**(-2)*t2s**(-3) + 64*s*m1**2*s4**(-2)*t2s**(-1)
     +     + 256*s*m1**2*s4**(-1)*u2s**(-1)*t2s**(-1) - 320*s*m1**2*
     +    s4**(-1)*t2s**(-2) + 256*s*m1**4*s4**(-2)*u2s**(-1)*t2s**(-1)
     +     - 320*s*m1**4*s4**(-2)*t2s**(-2) - 768*s*m1**4*s4**(-1)*
     +    u2s**(-1)*t2s**(-2) + 384*s*m1**4*s4**(-1)*t2s**(-3) - 768*s*
     +    m1**6*s4**(-2)*u2s**(-1)*t2s**(-2) + 384*s*m1**6*s4**(-2)*
     +    t2s**(-3) - 256*s*m1**6*s4**(-1)*u2s**(-2)*t2s**(-2) + 768*s*
     +    m1**6*s4**(-1)*u2s**(-1)*t2s**(-3) - 256*s*m1**8*s4**(-2)*
     +    u2s**(-2)*t2s**(-2) + 768*s*m1**8*s4**(-2)*u2s**(-1)*
     +    t2s**(-3) )
      MM_qg = MM_qg + Ar2*Nc**(-1)*st**2*Pi**3*alphas**2*hardfac*
     + log_all * ( 384*s*m1**8*s4**(-1)*u2s**(-2)*t2s**(-3) + 384*s*
     +    m1**10*s4**(-2)*u2s**(-2)*t2s**(-3) + 64*s*s4**(-1)*t2s**(-1)
     +     + 128*s**2*(t2*u2+s*m12)**(-2)*m1**6*u2s**(-1)*t2s**(-1) - 
     +    384*s**2*(t2*u2+s*m12)**(-2)*m1**6*t2s**(-2) + 128*s**2*
     +    (t2*u2+s*m12)**(-2)*m1**8*s4**(-1)*u2s**(-1)*t2s**(-1) - 384*
     +    s**2*(t2*u2+s*m12)**(-2)*m1**8*s4**(-1)*t2s**(-2) - 384*s**2*
     +    (t2*u2+s*m12)**(-2)*m1**8*u2s**(-1)*t2s**(-2) - 384*s**2*
     +    (t2*u2+s*m12)**(-2)*m1**10*s4**(-1)*u2s**(-1)*t2s**(-2) + 128
     +    *s**2*(t2*u2+s*m12)**(-1)*m1**4*u2s**(-1)*t2s**(-2) + 128*
     +    s**2*(t2*u2+s*m12)**(-1)*m1**6*s4**(-1)*u2s**(-1)*t2s**(-2)
     +     + 128*s**2*(t2*u2+s*m12)**(-1)*m1**6*u2s**(-2)*t2s**(-2) - 
     +    256*s**2*(t2*u2+s*m12)**(-1)*m1**6*u2s**(-1)*t2s**(-3) + 128*
     +    s**2*(t2*u2+s*m12)**(-1)*m1**8*s4**(-1)*u2s**(-2)*t2s**(-2)
     +     - 256*s**2*(t2*u2+s*m12)**(-1)*m1**8*s4**(-1)*u2s**(-1)*
     +    t2s**(-3) )
      MM_qg = MM_qg + Ar2*Nc**(-1)*st**2*Pi**3*alphas**2*hardfac*
     + log_all * (  - 384*s**2*(t2*u2+s*m12)**(-1)*m1**8*u2s**(-2)*
     +    t2s**(-3) - 384*s**2*(t2*u2+s*m12)**(-1)*m1**10*s4**(-1)*
     +    u2s**(-2)*t2s**(-3) - 128*s**2*m1**2*s4**(-1)*u2s**(-1)*
     +    t2s**(-2) - 128*s**2*m1**4*s4**(-2)*u2s**(-1)*t2s**(-2) - 128
     +    *s**2*m1**4*s4**(-1)*u2s**(-2)*t2s**(-2) + 256*s**2*m1**4*
     +    s4**(-1)*u2s**(-1)*t2s**(-3) - 128*s**2*m1**6*s4**(-2)*
     +    u2s**(-2)*t2s**(-2) + 256*s**2*m1**6*s4**(-2)*u2s**(-1)*
     +    t2s**(-3) + 384*s**2*m1**6*s4**(-1)*u2s**(-2)*t2s**(-3) + 384
     +    *s**2*m1**8*s4**(-2)*u2s**(-2)*t2s**(-3) - 128*s**3*
     +    (t2*u2+s*m12)**(-2)*m1**6*u2s**(-1)*t2s**(-2) - 128*s**3*
     +    (t2*u2+s*m12)**(-2)*m1**8*s4**(-1)*u2s**(-1)*t2s**(-2) - 128*
     +    s**3*(t2*u2+s*m12)**(-1)*m1**6*u2s**(-2)*t2s**(-3) - 128*s**3
     +    *(t2*u2+s*m12)**(-1)*m1**8*s4**(-1)*u2s**(-2)*t2s**(-3) + 128
     +    *s**3*m1**4*s4**(-1)*u2s**(-2)*t2s**(-3) + 128*s**3*m1**6*
     +    s4**(-2)*u2s**(-2)*t2s**(-3) )
      MM_qg = MM_qg + Ar2*Nc**(-1)*st**2*Pi**3*alphas**2*hardfac*
     + log_all * ( 256*(s+u2)**(-1)*m1**2*s4**(-1) + 128*(s+u2)**(-1)*
     +    m1**4*s4**(-2) + 128*(s+u2)**(-1) + 128*(t2*u2+s*m12)**(-2)*
     +    m1**4*u2 + 128*(t2*u2+s*m12)**(-2)*m1**6*u2*s4**(-1) + 128*
     +    (t2*u2+s*m12)**(-2)*m1**6*u2*t2s**(-1) - 128*
     +    (t2*u2+s*m12)**(-2)*m1**6*u2**2*t2s**(-2) + 128*
     +    (t2*u2+s*m12)**(-2)*m1**8*u2*s4**(-1)*t2s**(-1) - 128*
     +    (t2*u2+s*m12)**(-2)*m1**8*u2*t2s**(-2) - 128*
     +    (t2*u2+s*m12)**(-2)*m1**8*u2**2*s4**(-1)*t2s**(-2) + 128*
     +    (t2*u2+s*m12)**(-2)*m1**8*t2s**(-1) - 128*(t2*u2+s*m12)**(-2)
     +    *m1**10*u2*s4**(-1)*t2s**(-2) + 128*(t2*u2+s*m12)**(-2)*
     +    m1**10*s4**(-1)*t2s**(-1) + 128*(t2*u2+s*m12)**(-2)*m1**10*
     +    u2s**(-1)*t2s**(-1) - 128*(t2*u2+s*m12)**(-2)*m1**10*
     +    t2s**(-2) + 128*(t2*u2+s*m12)**(-2)*m1**12*s4**(-1)*u2s**(-1)
     +    *t2s**(-1) - 128*(t2*u2+s*m12)**(-2)*m1**12*s4**(-1)*
     +    t2s**(-2) )
      MM_qg = MM_qg + Ar2*Nc**(-1)*st**2*Pi**3*alphas**2*hardfac*
     + log_all * (  - 128*(t2*u2+s*m12)**(-2)*m1**12*u2s**(-1)*
     +    t2s**(-2) - 128*(t2*u2+s*m12)**(-2)*m1**14*s4**(-1)*u2s**(-1)
     +    *t2s**(-2) - 256*(t2*u2+s*m12)**(-1)*m1**2 + 256*
     +    (t2*u2+s*m12)**(-1)*m1**4*u2*t2s**(-2) - 256*
     +    (t2*u2+s*m12)**(-1)*m1**4*s4**(-1) - 128*(t2*u2+s*m12)**(-1)*
     +    m1**4*u2s**(-1) - 256*(t2*u2+s*m12)**(-1)*m1**4*t2s**(-1) + 
     +    256*(t2*u2+s*m12)**(-1)*m1**6*u2*s4**(-1)*t2s**(-2) - 384*
     +    (t2*u2+s*m12)**(-1)*m1**6*u2*t2s**(-3) - 128*
     +    (t2*u2+s*m12)**(-1)*m1**6*s4**(-1)*u2s**(-1) - 256*
     +    (t2*u2+s*m12)**(-1)*m1**6*s4**(-1)*t2s**(-1) - 384*
     +    (t2*u2+s*m12)**(-1)*m1**6*u2s**(-1)*t2s**(-1) + 384*
     +    (t2*u2+s*m12)**(-1)*m1**6*t2s**(-2) + 128*(t2*u2+s*m12)**(-1)
     +    *m1**8*t2**(-1)*u2*t2s**(-3) + 128*(t2*u2+s*m12)**(-1)*m1**8*
     +    t2**(-1)*u2s**(-1)*t2s**(-1) - 384*(t2*u2+s*m12)**(-1)*m1**8*
     +    u2*s4**(-1)*t2s**(-3) )
      MM_qg = MM_qg + Ar2*Nc**(-1)*st**2*Pi**3*alphas**2*hardfac*
     + log_all * (  - 384*(t2*u2+s*m12)**(-1)*m1**8*s4**(-1)*u2s**(-1)*
     +    t2s**(-1) + 384*(t2*u2+s*m12)**(-1)*m1**8*s4**(-1)*t2s**(-2)
     +     + 512*(t2*u2+s*m12)**(-1)*m1**8*u2s**(-1)*t2s**(-2) - 512*
     +    (t2*u2+s*m12)**(-1)*m1**8*t2s**(-3) + 128*(t2*u2+s*m12)**(-1)
     +    *m1**10*t2**(-1)*u2*s4**(-1)*t2s**(-3) + 128*
     +    (t2*u2+s*m12)**(-1)*m1**10*t2**(-1)*s4**(-1)*u2s**(-1)*
     +    t2s**(-1) + 128*(t2*u2+s*m12)**(-1)*m1**10*t2**(-1)*t2s**(-3)
     +     + 512*(t2*u2+s*m12)**(-1)*m1**10*s4**(-1)*u2s**(-1)*
     +    t2s**(-2) - 512*(t2*u2+s*m12)**(-1)*m1**10*s4**(-1)*t2s**(-3)
     +     + 128*(t2*u2+s*m12)**(-1)*m1**10*u2s**(-2)*t2s**(-2) - 640*
     +    (t2*u2+s*m12)**(-1)*m1**10*u2s**(-1)*t2s**(-3) + 128*
     +    (t2*u2+s*m12)**(-1)*m1**12*t2**(-1)*s4**(-1)*t2s**(-3) + 128*
     +    (t2*u2+s*m12)**(-1)*m1**12*t2**(-1)*u2s**(-1)*t2s**(-3) + 128
     +    *(t2*u2+s*m12)**(-1)*m1**12*s4**(-1)*u2s**(-2)*t2s**(-2) - 
     +    640*(t2*u2+s*m12)**(-1)*m1**12*s4**(-1)*u2s**(-1)*t2s**(-3) )
