     + log_all * (  - 128*(t2*u2+s*m12)**(-1)*m1**12*u2s**(-2)*
     +    t2s**(-3) + 128*(t2*u2+s*m12)**(-1)*m1**14*t2**(-1)*s4**(-1)*
