      real*8 function BB_GGV(s,t1,ms1,mg,mt,ms,ms2,mx1,mx2,
     &                       s2t,c2t,c2b,susy)

      implicit none 

      real*8 Pi,Nc,Co,Ck,Cqed,Nf,Ns,alphas,fac,susy
      real*8 s,t1,u1,ms1,mg,mt,ms,ms2,s2t,c2t,t,u,ms12,mx1,mx2
      real*8 m2(2,8),softin(4),c2b
      real*8 SK3B0A(7),SK3B0B(8),SK3B0C(3),SK3B0D(3,2),SK3BP(4) 
      real*8 SK3C0A(10),SK3C0B(6),SK3C0C(8,2),SK3D0(11,2)
      real*8 SOF1(8)
      real*8 mur,dec,TB_GGB,STGGV

      Pi=4.D0*atan(1.D0)
      Nc = 3.D0
      Co = Nc*(Nc**2-1.D0)
      Ck = (Nc**2-1.D0)/Nc 
      Cqed = Nc**2 - 1/Nc**2 
      Nf = 6.D0
      Ns = 6.D0
      alphas = 1.D0

      u1 = - s - t1 
      t  = t1 + ms1**2 
      u  = u1 + ms1**2 
      ms12 = ms1**2 
      fac = 1.D0/(64*Pi*(Nc**2-1.D0)**2)

      m2(1,1) = s 
      m2(1,2) = t1 + ms1**2  
      m2(1,3) = u1 + ms1**2 
      m2(1,4) = t1
      m2(1,5) = u1
      m2(2,1) = ms1**2 
      m2(2,2) = ms2**2 
      m2(2,3) = ms**2 
      m2(2,4) = mg**2 
      m2(2,5) = mt**2 
      m2(2,6) = ms1**2 
      m2(2,7) = mx1**2
      m2(2,8) = mx2**2

      call SCALAR_ARRAY_TB_B_GS(m2,SK3B0A,SK3B0B,SK3B0C,SK3B0D,SK3BP)
      call SCALAR_ARRAY_TB_C_GS(m2,SK3C0A,SK3C0B,SK3C0C,SK3D0)

      softin(1) = s
      softin(2) = t1 
      softin(3) = u1 
      softin(4) = ms1**2 

      call SOFT_ARRAY_LQ(softin,SOF1)

      mur = ms1
      
      dec = 2.D0*alphas/Pi * TB_GGB(s,t1,ms1)*
     &      (  (Ns-2.D0)/12.D0 * susy * log(mur**2/ms**2)
     &       + 1.D0/24.D0             * log(mur**2/ms1**2)
     &       + 1.D0/24.D0      * susy * log(mur**2/ms2**2)
     &       + 1.D0/24.D0      * susy * log(mur**2/mx1**2)
     &       + 1.D0/24.D0      * susy * log(mur**2/mx2**2)
     &       + 1.D0/6.D0              * log(mur**2/mt**2) 
     &       + Nc/6.D0         * susy * log(mur**2/mg**2)   )

      STGGV =
     &  + Cqed*Pi*alphas**3 * ( 4*ms1**2*s**(-2)*t**(-1) + 4*ms1**2*
     &    s**(-2)*u**(-1) - 12*ms1**2*s**(-2)*t1**(-1) - 12*ms1**2*
     &    s**(-2)*u1**(-1) + 4*ms1**2*s**(-1)*t**(-1)*t1**(-1) + 4*
     &    ms1**2*s**(-1)*t**(-1)*u1**(-1) + 4*ms1**2*s**(-1)*u**(-1)*
     &    t1**(-1) + 4*ms1**2*s**(-1)*u**(-1)*u1**(-1) - 4*ms1**2*
     &    s**(-1)*t1**(-2) - 4*ms1**2*s**(-1)*u1**(-2) + 40*ms1**4*
     &    s**(-2)*t**(-1)*t1**(-1) + 20*ms1**4*s**(-2)*t**(-1)*u1**(-1)
     &     + 20*ms1**4*s**(-2)*u**(-1)*t1**(-1) + 40*ms1**4*s**(-2)*
     &    u**(-1)*u1**(-1) - 40*ms1**4*s**(-2)*t1**(-2) - 48*ms1**4*
     &    s**(-2)*t1**(-1)*u1**(-1) - 40*ms1**4*s**(-2)*u1**(-2) + 4*
     &    ms1**4*s**(-1)*t**(-1)*t1**(-2) + 4*ms1**4*s**(-1)*u**(-1)*
     &    u1**(-2) + 72*ms1**6*s**(-2)*t**(-1)*t1**(-2) + 56*ms1**6*
     &    s**(-2)*t**(-1)*t1**(-1)*u1**(-1) + 56*ms1**6*s**(-2)*u**(-1)
     &    *t1**(-1)*u1**(-1) + 72*ms1**6*s**(-2)*u**(-1)*u1**(-2) - 32*
     &    ms1**6*s**(-2)*t1**(-3) - 32*ms1**6*s**(-2)*t1**(-2)*u1**(-1)
     &     - 32*ms1**6*s**(-2)*t1**(-1)*u1**(-2) )
      STGGV = STGGV + Cqed*Pi*alphas**3 * (  - 32*ms1**6*s**(-2)*
     &    u1**(-3) + 32*ms1**8*s**(-2)*t**(-1)*t1**(-3) + 32*ms1**8*
     &    s**(-2)*t**(-1)*t1**(-2)*u1**(-1) + 32*ms1**8*s**(-2)*u**(-1)
     &    *t1**(-1)*u1**(-2) + 32*ms1**8*s**(-2)*u**(-1)*u1**(-3) )
      STGGV = STGGV + Cqed*s2t**2*Pi*alphas**3 * ( 8*ms1**2*s**(-2)*
     &    t1**(-1) + 8*ms1**2*s**(-2)*u1**(-1) )
      STGGV = STGGV + Ck*Pi*alphas**3 * ( 16*ms1**2*s**(-2)*t1**(-1) + 
     &    16*ms1**2*s**(-2)*u1**(-1) )
      STGGV = STGGV + Ck*s2t**2*Pi*alphas**3 * (  - 16*ms1**2*s**(-2)*
     &    t1**(-1) - 16*ms1**2*s**(-2)*u1**(-1) )
      STGGV = STGGV + Co*Pi*alphas**3 * ( 16./3.*ms1**2*s**(-3) - 8./3.
     &    *ms1**2*s**(-2)*t1**(-1) - 8./3.*ms1**2*s**(-2)*u1**(-1) )
      STGGV = STGGV + Co*s2t**2*Pi*alphas**3 * ( 4*ms1**2*s**(-2)*
     &    t1**(-1) + 4*ms1**2*s**(-2)*u1**(-1) )
      STGGV = STGGV + Co*(Nf-1)*Pi*alphas**3 * ( 32./3.*ms1**2*s**(-3)
     &     + 8./3.*ms1**2*s**(-2)*t1**(-1) + 8./3.*ms1**2*s**(-2)*
     &    u1**(-1) )
      STGGV = STGGV + Nc*Ck*Pi*alphas**3 * (  - 4*ms1**2*s**(-3)*
     &    t**(-1)*t1 + 4*ms1**2*s**(-3)*u**(-1)*t1 + 8*ms1**2*s**(-3)
     &     + 6*ms1**2*s**(-2)*t**(-1) + 10*ms1**2*s**(-2)*u**(-1) - 30*
     &    ms1**2*s**(-2)*t1**(-1) - 30*ms1**2*s**(-2)*u1**(-1) - 12*
     &    ms1**2*s**(-1)*t**(-1)*t1**(-1) - 16*ms1**2*s**(-1)*t**(-1)*
     &    u1**(-1) - 16*ms1**2*s**(-1)*u**(-1)*t1**(-1) - 12*ms1**2*
     &    s**(-1)*u**(-1)*u1**(-1) + 12*ms1**2*s**(-1)*t1**(-2) + 12*
     &    ms1**2*s**(-1)*u1**(-2) - 20*ms1**4*s**(-3)*t**(-1) - 20*
     &    ms1**4*s**(-3)*u**(-1) + 16*ms1**4*s**(-3)*t1**(-1) + 16*
     &    ms1**4*s**(-3)*u1**(-1) - 18*ms1**4*s**(-2)*t**(-1)*t1**(-1)
     &     + 24*ms1**4*s**(-2)*t**(-1)*u1**(-1) + 24*ms1**4*s**(-2)*
     &    u**(-1)*t1**(-1) - 18*ms1**4*s**(-2)*u**(-1)*u1**(-1) + 40*
     &    ms1**4*s**(-2)*t1**(-2) - 16*ms1**4*s**(-2)*t1**(-1)*u1**(-1)
     &     + 40*ms1**4*s**(-2)*u1**(-2) - 20*ms1**4*s**(-1)*t**(-1)*
     &    t1**(-2) - 20*ms1**4*s**(-1)*u**(-1)*u1**(-2) + 8*ms1**4*
     &    s**(-1)*t1**(-3) )
      STGGV = STGGV + Nc*Ck*Pi*alphas**3 * ( 8*ms1**4*s**(-1)*u1**(-3)
     &     - 16*ms1**6*s**(-3)*t**(-1)*t1**(-1) - 16*ms1**6*s**(-3)*
     &    u**(-1)*u1**(-1) - 104*ms1**6*s**(-2)*t**(-1)*t1**(-2) - 24*
     &    ms1**6*s**(-2)*t**(-1)*t1**(-1)*u1**(-1) - 24*ms1**6*s**(-2)*
     &    u**(-1)*t1**(-1)*u1**(-1) - 104*ms1**6*s**(-2)*u**(-1)*
     &    u1**(-2) + 64*ms1**6*s**(-2)*t1**(-3) + 32*ms1**6*s**(-2)*
     &    t1**(-2)*u1**(-1) + 32*ms1**6*s**(-2)*t1**(-1)*u1**(-2) + 64*
     &    ms1**6*s**(-2)*u1**(-3) - 8*ms1**6*s**(-1)*t**(-1)*t1**(-3)
     &     - 8*ms1**6*s**(-1)*u**(-1)*u1**(-3) - 64*ms1**8*s**(-2)*
     &    t**(-1)*t1**(-3) - 32*ms1**8*s**(-2)*t**(-1)*t1**(-2)*
     &    u1**(-1) - 32*ms1**8*s**(-2)*u**(-1)*t1**(-1)*u1**(-2) - 64*
     &    ms1**8*s**(-2)*u**(-1)*u1**(-3) )
      STGGV = STGGV + Nc*Ck*s2t**2*Pi*alphas**3 * (  - 4*ms1**2*s**(-2)
     &    *t1**(-1) - 4*ms1**2*s**(-2)*u1**(-1) )
      STGGV = STGGV + Nc*Co*Pi*alphas**3 * ( ms1**2*s**(-3)*t**(-1)*t1
     &     - ms1**2*s**(-3)*u**(-1)*t1 - 38./3.*ms1**2*s**(-3) + 3*
     &    ms1**2*s**(-2)*t**(-1) + 2*ms1**2*s**(-2)*u**(-1) - 41./3.*
     &    ms1**2*s**(-2)*t1**(-1) - 41./3.*ms1**2*s**(-2)*u1**(-1) + 5*
     &    ms1**2*s**(-1)*t**(-1)*t1**(-1) + 5*ms1**2*s**(-1)*u**(-1)*
     &    u1**(-1) - 5*ms1**2*s**(-1)*t1**(-2) - 5*ms1**2*s**(-1)*
     &    u1**(-2) + 5*ms1**4*s**(-3)*t**(-1) + 5*ms1**4*s**(-3)*
     &    u**(-1) - 4*ms1**4*s**(-3)*t1**(-1) - 4*ms1**4*s**(-3)*
     &    u1**(-1) + 3*ms1**4*s**(-2)*t**(-1)*t1**(-1) + 3*ms1**4*
     &    s**(-2)*u**(-1)*u1**(-1) + 11*ms1**4*s**(-1)*t**(-1)*t1**(-2)
     &     + 11*ms1**4*s**(-1)*u**(-1)*u1**(-2) - 6*ms1**4*s**(-1)*
     &    t1**(-3) - 6*ms1**4*s**(-1)*u1**(-3) + 4*ms1**6*s**(-3)*
     &    t**(-1)*t1**(-1) + 4*ms1**6*s**(-3)*u**(-1)*u1**(-1) + 6*
     &    ms1**6*s**(-1)*t**(-1)*t1**(-3) + 6*ms1**6*s**(-1)*u**(-1)*
     &    u1**(-3) )
      STGGV = STGGV + susy*Cqed*Pi*alphas**3 * ( 16*ms1**2*s**(-2)*
     &    t1**(-1) + 16*ms1**2*s**(-2)*u1**(-1) )
      STGGV = STGGV + susy*Cqed*s2t**2*Pi*alphas**3 * (  - 8*ms1**2*
     &    s**(-2)*t1**(-1) - 8*ms1**2*s**(-2)*u1**(-1) )
      STGGV = STGGV + susy*Ck*Pi*alphas**3 * (  - 16*ms1**2*s**(-2)*
     &    t1**(-1) - 16*ms1**2*s**(-2)*u1**(-1) )
      STGGV = STGGV + susy*Ck*s2t**2*Pi*alphas**3 * ( 16*ms1**2*s**(-2)
     &    *t1**(-1) + 16*ms1**2*s**(-2)*u1**(-1) )
      STGGV = STGGV + susy*Co*Pi*alphas**3 * (  - 16*ms1**2*s**(-3) )
      STGGV = STGGV + susy*Co*s2t**2*Pi*alphas**3 * (  - 4*ms1**2*
     &    s**(-2)*t1**(-1) - 4*ms1**2*s**(-2)*u1**(-1) )
      STGGV = STGGV + susy*Co*(Ns-2)*Pi*alphas**3 * (  - 32./3.*ms1**2*
     &    s**(-3) - 8./3.*ms1**2*s**(-2)*t1**(-1) - 8./3.*ms1**2*
     &    s**(-2)*u1**(-1) )
      STGGV = STGGV + susy*Nc*Ck*Pi*alphas**3 * (  - 8*ms1**2*s**(-2)*
     &    t1**(-1) - 8*ms1**2*s**(-2)*u1**(-1) )
      STGGV = STGGV + susy*Nc*Ck*s2t**2*Pi*alphas**3 * ( 4*ms1**2*
     &    s**(-2)*t1**(-1) + 4*ms1**2*s**(-2)*u1**(-1) )
      STGGV = STGGV + susy*Nc*Co*Pi*alphas**3 * (  - 8*(s-4*ms12)**(-1)
     &    *mg**2*ms1**(-2)*s**(-1) + 4*(s-4*ms12)**(-1)*mg**2*ms1**(-2)
     &    *t1**(-1) + 4*(s-4*ms12)**(-1)*mg**2*ms1**(-2)*u1**(-1) + 32*
     &    (s-4*ms12)**(-1)*mg**2*s**(-2) - 16*(s-4*ms12)**(-1)*mg**2*
     &    s**(-1)*t1**(-1) - 16*(s-4*ms12)**(-1)*mg**2*s**(-1)*u1**(-1)
     &     + 32./3.*ms1**2*s**(-3) + 32./3.*ms1**2*s**(-2)*t1**(-1) + 
     &    32./3.*ms1**2*s**(-2)*u1**(-1) + 8*mg**2*ms1**(-2)*s**(-2) - 
     &    4*mg**2*ms1**(-2)*s**(-1)*t1**(-1) - 4*mg**2*ms1**(-2)*
     &    s**(-1)*u1**(-1) - 16*mg**2*s**(-3)*t**(-1)*t1 + 16*mg**2*
     &    s**(-3)*u**(-1)*t1 + 32*mg**2*s**(-3) - 40*mg**2*s**(-2)*
     &    t**(-1) - 24*mg**2*s**(-2)*u**(-1) + 40*mg**2*s**(-2)*
     &    t1**(-1) + 40*mg**2*s**(-2)*u1**(-1) - 16*mg**2*s**(-1)*
     &    t**(-1)*t1**(-1) - 16*mg**2*s**(-1)*u**(-1)*u1**(-1) + 16*
     &    mg**2*s**(-1)*t1**(-2) + 16*mg**2*s**(-1)*u1**(-2) - 32*mg**2
     &    *ms1**2*s**(-3)*t**(-1) - 32*mg**2*ms1**2*s**(-3)*u**(-1) + 
     &    16*mg**2*ms1**2*s**(-3)*t1**(-1) )
      STGGV = STGGV + susy*Nc*Co*Pi*alphas**3 * ( 16*mg**2*ms1**2*
     &    s**(-3)*u1**(-1) - 72*mg**2*ms1**2*s**(-2)*t**(-1)*t1**(-1)
     &     - 72*mg**2*ms1**2*s**(-2)*u**(-1)*u1**(-1) + 32*mg**2*ms1**2
     &    *s**(-2)*t1**(-2) + 32*mg**2*ms1**2*s**(-2)*u1**(-2) - 32*
     &    mg**2*ms1**2*s**(-1)*t**(-1)*t1**(-2) - 32*mg**2*ms1**2*
     &    s**(-1)*u**(-1)*u1**(-2) + 16*mg**2*ms1**2*s**(-1)*t1**(-3)
     &     + 16*mg**2*ms1**2*s**(-1)*u1**(-3) - 16*mg**2*ms1**4*s**(-3)
     &    *t**(-1)*t1**(-1) - 16*mg**2*ms1**4*s**(-3)*u**(-1)*u1**(-1)
     &     - 32*mg**2*ms1**4*s**(-2)*t**(-1)*t1**(-2) - 32*mg**2*ms1**4
     &    *s**(-2)*u**(-1)*u1**(-2) - 16*mg**2*ms1**4*s**(-1)*t**(-1)*
     &    t1**(-3) - 16*mg**2*ms1**4*s**(-1)*u**(-1)*u1**(-3) )
      STGGV = STGGV + SK3B0A(1)*susy*Nc*Ck*Pi*alphas**3 * ( 32./3.*
     &    s**(-2) + 64./3.*ms1**2*s**(-2)*t1**(-1) + 64./3.*ms1**2*
     &    s**(-2)*u1**(-1) + 64./3.*ms1**4*s**(-2)*t1**(-2) + 128./3.*
     &    ms1**4*s**(-2)*t1**(-1)*u1**(-1) + 64./3.*ms1**4*s**(-2)*
     &    u1**(-2) )
      STGGV = STGGV + SK3B0A(1)*susy*Nc*Co*Pi*alphas**3 * (  - 8*
     &    (s-4*ms12)**(-1)*mg**2*ms1**(-2)*s**(-1) + 4*(s-4*ms12)**(-1)
     &    *mg**2*ms1**(-2)*t1**(-1) + 4*(s-4*ms12)**(-1)*mg**2*
     &    ms1**(-2)*u1**(-1) + 32*(s-4*ms12)**(-1)*mg**2*s**(-2) - 16*
     &    (s-4*ms12)**(-1)*mg**2*s**(-1)*t1**(-1) - 16*(s-4*ms12)**(-1)
     &    *mg**2*s**(-1)*u1**(-1) - 64./3.*s**(-4)*t1**2 - 64./3.*
     &    s**(-3)*t1 - 32./3.*s**(-2) - 128./3.*ms1**2*s**(-3) - 64./3.
     &    *ms1**2*s**(-2)*t1**(-1) - 64./3.*ms1**2*s**(-2)*u1**(-1) - 
     &    64./3.*ms1**4*s**(-2)*t1**(-2) - 64./3.*ms1**4*s**(-2)*
     &    u1**(-2) + 8*mg**2*ms1**(-2)*s**(-2) - 4*mg**2*ms1**(-2)*
     &    s**(-1)*t1**(-1) - 4*mg**2*ms1**(-2)*s**(-1)*u1**(-1) - 16*
     &    mg**2*s**(-3)*t**(-1)*t1 + 16*mg**2*s**(-3)*u**(-1)*t1 + 32*
     &    mg**2*s**(-3) - 40*mg**2*s**(-2)*t**(-1) - 24*mg**2*s**(-2)*
     &    u**(-1) + 40*mg**2*s**(-2)*t1**(-1) + 40*mg**2*s**(-2)*
     &    u1**(-1) - 16*mg**2*s**(-1)*t**(-1)*t1**(-1) - 16*mg**2*
     &    s**(-1)*u**(-1)*u1**(-1) )
      STGGV = STGGV + SK3B0A(1)*susy*Nc*Co*Pi*alphas**3 * ( 16*mg**2*
     &    s**(-1)*t1**(-2) + 16*mg**2*s**(-1)*u1**(-2) - 128*mg**2*
     &    ms1**2*s**(-4) - 32*mg**2*ms1**2*s**(-3)*t**(-1) - 32*mg**2*
     &    ms1**2*s**(-3)*u**(-1) - 80./3.*mg**2*ms1**2*s**(-3)*t1**(-1)
     &     - 80./3.*mg**2*ms1**2*s**(-3)*u1**(-1) - 72*mg**2*ms1**2*
     &    s**(-2)*t**(-1)*t1**(-1) - 72*mg**2*ms1**2*s**(-2)*u**(-1)*
     &    u1**(-1) + 32*mg**2*ms1**2*s**(-2)*t1**(-2) - 32./3.*mg**2*
     &    ms1**2*s**(-2)*t1**(-1)*u1**(-1) + 32*mg**2*ms1**2*s**(-2)*
     &    u1**(-2) - 32*mg**2*ms1**2*s**(-1)*t**(-1)*t1**(-2) - 32*
     &    mg**2*ms1**2*s**(-1)*u**(-1)*u1**(-2) + 16*mg**2*ms1**2*
     &    s**(-1)*t1**(-3) + 16*mg**2*ms1**2*s**(-1)*u1**(-3) - 16*
     &    mg**2*ms1**4*s**(-3)*t**(-1)*t1**(-1) - 16*mg**2*ms1**4*
     &    s**(-3)*u**(-1)*u1**(-1) - 32*mg**2*ms1**4*s**(-2)*t**(-1)*
     &    t1**(-2) - 32*mg**2*ms1**4*s**(-2)*u**(-1)*u1**(-2) - 16*
     &    mg**2*ms1**4*s**(-1)*t**(-1)*t1**(-3) - 16*mg**2*ms1**4*
     &    s**(-1)*u**(-1)*u1**(-3) )
      STGGV = STGGV + SK3B0A(2)*Ck*Pi*alphas**3 * ( 32./3.*s**(-2) + 64.
     &    /3.*ms1**2*s**(-2)*t1**(-1) + 64./3.*ms1**2*s**(-2)*u1**(-1)
     &     + 64./3.*ms1**4*s**(-2)*t1**(-2) + 128./3.*ms1**4*s**(-2)*
     &    t1**(-1)*u1**(-1) + 64./3.*ms1**4*s**(-2)*u1**(-2) )
      STGGV = STGGV + SK3B0A(2)*Co*Pi*alphas**3 * (  - 64./3.*s**(-4)*
     &    t1**2 - 64./3.*s**(-3)*t1 - 32./3.*s**(-2) - 128./3.*ms1**2*
     &    s**(-3) - 64./3.*ms1**2*s**(-2)*t1**(-1) - 64./3.*ms1**2*
     &    s**(-2)*u1**(-1) - 64./3.*ms1**4*s**(-2)*t1**(-2) - 64./3.*
     &    ms1**4*s**(-2)*u1**(-2) - 128*mt**2*ms1**2*s**(-4) - 128./3.*
     &    mt**2*ms1**2*s**(-3)*t1**(-1) - 128./3.*mt**2*ms1**2*s**(-3)*
     &    u1**(-1) - 32./3.*mt**2*ms1**2*s**(-2)*t1**(-1)*u1**(-1) )
      STGGV = STGGV + SK3B0A(4)*susy*Ck*(Ns-2)*Pi*alphas**3 * ( 16./3.*
     &    s**(-2) + 32./3.*ms1**2*s**(-2)*t1**(-1) + 32./3.*ms1**2*
     &    s**(-2)*u1**(-1) + 32./3.*ms1**4*s**(-2)*t1**(-2) + 64./3.*
     &    ms1**4*s**(-2)*t1**(-1)*u1**(-1) + 32./3.*ms1**4*s**(-2)*
     &    u1**(-2) )
      STGGV = STGGV + SK3B0A(4)*susy*Co*(Ns-2)*Pi*alphas**3 * (  - 32./
     &    3.*s**(-4)*t1**2 - 32./3.*s**(-3)*t1 - 16./3.*s**(-2) - 64./3.
     &    *ms1**2*s**(-3) - 32./3.*ms1**2*s**(-2)*t1**(-1) - 32./3.*
     &    ms1**2*s**(-2)*u1**(-1) - 32./3.*ms1**4*s**(-2)*t1**(-2) - 32.
     &    /3.*ms1**4*s**(-2)*u1**(-2) + 128*ms**2*ms1**2*s**(-4) + 128./
     &    3.*ms**2*ms1**2*s**(-3)*t1**(-1) + 128./3.*ms**2*ms1**2*
     &    s**(-3)*u1**(-1) + 32./3.*ms**2*ms1**2*s**(-2)*t1**(-1)*
     &    u1**(-1) )
      STGGV = STGGV + SK3B0A(5)*susy*Ck*Pi*alphas**3 * ( 8./3.*s**(-2)
     &     + 16./3.*ms1**2*s**(-2)*t1**(-1) + 16./3.*ms1**2*s**(-2)*
     &    u1**(-1) + 16./3.*ms1**4*s**(-2)*t1**(-2) + 32./3.*ms1**4*
     &    s**(-2)*t1**(-1)*u1**(-1) + 16./3.*ms1**4*s**(-2)*u1**(-2) )
      STGGV = STGGV + SK3B0A(5)*susy*Co*Pi*alphas**3 * (  - 16./3.*
     &    s**(-4)*t1**2 - 16./3.*s**(-3)*t1 - 8./3.*s**(-2) - 32./3.*
